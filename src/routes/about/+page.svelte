        <h2 class="section-title">Das Sammelkartenspiel</h2>
        <p>
          Das Yu-Gi-Oh! Trading Card Game (TCG) wurde 1999 in Japan eingeführt und eroberte ab 2002 den internationalen Markt. Es ist eines der am längsten laufenden und erfolgreichsten Sammelkartenspiele der Welt mit über 35 Milliarden verkauften Karten.
        </p>
        <p>
          Im Spiel duellieren sich zwei Spieler mit ihren individuell zusammengestellten Decks. Das Ziel ist es, die Lebenspunkte des Gegners auf Null zu reduzieren oder ihn spielunfähig zu machen. Mit Monstern, Zauber- und Fallenkarten entwickeln Spieler komplexe Strategien und Kombos.
        </p>
